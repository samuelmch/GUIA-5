    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        double l1=Double.parseDouble(txtl1.getText());
         double l2=Double.parseDouble(txtl2.getText());
          double l3=Double.parseDouble(txtl3.getText());
           double l4=Double.parseDouble(txtl4.getText());
           int figura = 0;
           switch (figura){
               
               case 0 :{
                   if(l1==l2 && l1==l3 && l1==l4){
                       lblarea4.setText(String.valueOf(l1*l1));
                       lbldiagonal4.setText(String.valueOf(Math.sqrt((l1*l1)+(l1*l1))));
                   }
                   else if (l1==l3 && l2==l4 ){
                       lblarea4.setText(String.valueOf(l1*l2));
                       lbldiagonal4.setText(String.valueOf(Math.sqrt((l1*l1)+(l2*l2))));
                   }else if (l2==l3 || l1!=l4 ){
                       lblarea4.setText(String.valueOf(((l1+l4)*3)/2));
                       lbldiagonal4.setEnabled(false);
                   }
                   break;
               }
               
               
           }
    }//GEN-LAST:event_jButton1ActionPerformed

